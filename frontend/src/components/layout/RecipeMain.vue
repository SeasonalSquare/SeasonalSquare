<template>
  <div>
    <v-tooltip bottom  v-for="(recipe, index) in recipes" :key="index" >
      <template v-slot:activator="{ on, attrs }">
        <v-avatar  style="margin-right:20px"  v-bind="attrs" v-on="on"
            size="90">
            <img :src="recipe.image"  @click="goRecipe(recipe.pk)" >
        </v-avatar>
      </template>
      <span>{{recipe.title}}</span>
    </v-tooltip>
  </div>
</template>

<script>
import http from '@/util/http-common.js'

export default {
  name: 'RecipeMain',
  props : ['food'],
  data() {
    return {
      name: '',
      recipes: [],
    }
  },
  created() {
    // console.log(">>>>>>>>>"+this.food);

    http.get(`/recipe/grocery/${this.food}`)
      .then(response => {
      this.recipes = response.data
      this.recipes.splice(3)
      })
      .catch(err => {
      console.log(err + "죽인다")
      })
  },
  methods: {
    goRecipe(pk){
        alert("recipe"+pk);
    }
  }
  
}
</script>

